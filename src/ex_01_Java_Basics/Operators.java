package ex_01_Java_Basics;

public class Operators {
    public static void main(String[] args) {

        System.out.println(2+6); // To Print sum of the numbers
        System.out.println(6-2); // To Print subtraction of the numbers
        System.out.println(2*6); // To Print multiplication of the numbers
        System.out.println(6/2); // To Print division of the numbers
    }
}

